import { NextResponse } from "next/server";
  import { 
    getProjectById,
    updateProject,
    deleteProject
  } from "@/lib/services/project";
  
  type RouteParams = Promise<{
    projectId: string;
  }>;
  
  export async function GET(request: Request, { params }: { params: RouteParams }) {
    try {
      const resolvedParams = await params;
      const project = await getProjectById(resolvedParams.projectId);
      return NextResponse.json(project);
    } catch (error: any) {
      return NextResponse.json(
        { error: error.message || "Bir hata oluştu" },
        { status: 500 }
      );
    }
  }
  
  export async function PUT(request: Request, { params }: { params: RouteParams }) {
    try {
      const resolvedParams = await params;
      const body = await request.json();
      const project = await updateProject(resolvedParams.projectId, body);
      return NextResponse.json(project);
    } catch (error: any) {
      return NextResponse.json(
        { error: error.message || "Bir hata oluştu" },
        { status: 500 }
      );
    }
  }
  
  export async function DELETE(request: Request, { params }: { params: RouteParams }) {
    try {
      const resolvedParams = await params;
      await deleteProject(resolvedParams.projectId);
      return NextResponse.json({ message: "Project başarıyla silindi" });
    } catch (error: any) {
      return NextResponse.json(
        { error: error.message || "Bir hata oluştu" },
        { status: 500 }
      );
    }
  }